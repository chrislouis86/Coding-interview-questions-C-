#include <iostream>
using namespace std; 


// 1. Write a program to check if wether a number is negative or positive. 
int main()
{
	int number;
	number = 100;
	if (number >= 0)
	{
		std::cout << " is a positive number. " << std::endl;
	}
	else  
	{
		std::cout << " is a negative number." << std::endl;
	}

	return 0;	


}
		
